<template>
  <div class="container">

    <toast></toast>

    <div class="row mb-3  ">
      <div class="col">
        <nav aria-label="breadcrumb">
          <ol class="breadcrumb">
            <li class="breadcrumb-item">
              <router-link :to="{ name: 'groupOverView' }">Groups</router-link>
            </li>
            <li class="breadcrumb-item active text-capitalize" aria-current="page">{{name}}</li>
          </ol>
        </nav>
      </div>
    </div>

    <div class="row mb-3  ">
      <div class="col">
        <h1>{{ 'security-ui-members-page-title' | i18n }}</h1>
      </div>
    </div>

    <div class="row">
      <div class="col">
        <button id="add-member-btn" @click="addMember" type="button" class="btn btn-primary float-right"><i class="fa fa-plus"></i> Add Member</button>
        <h3 class="mt-2">Members</h3>
      </div>
    </div>

    <div class="row groups-listing mt-1">
      <div class="col">
        <router-link
          v-for="member in sortedMembers"
          :key="member.username"
          :to="{ name: 'memberDetail', params: { groupName: name, memberName: member.username } }"
          class="list-group-item list-group-item-action">
          <div>
            <h5 class="text-capitalize">{{member.username}}  <small class="font-weight-light text-uppercase"> ({{member.roleLabel}})</small></h5>
          </div>
        </router-link>
      </div>
    </div>

  </div>
</template>

<script>
  import Toast from './Toast'
  import { mapGetters, mapMutations } from 'vuex'

  export default {
    name: 'GroupDetail',
    props: {
      name: {
        type: String,
        required: false
      }
    },
    computed: {
      ...mapGetters([
        'groupMembers'
      ]),
      sortedMembers () {
        const members = this.groupMembers[this.name] || []
        return [...members].sort((a, b) => a.username.localeCompare(b.username))
      }
    },
    methods: {
      ...mapMutations([
        'clearToast'
      ]),
      addMember () {
        this.clearToast()
        this.$router.push({name: 'addMember', params: { groupName: this.name }})
      }
    },
    created () {
      this.$store.dispatch('fetchGroupMembers', this.name)
    },
    components: {
      Toast
    }
  }
</script>
